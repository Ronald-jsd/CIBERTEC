package model;

public class Linea {
    private String codLinea;
    private String lineaNombre;
    private String maquina;
    private double capacidadNominal;
    
	public String getCodLinea() {
		return codLinea;
	}
	public void setCodLinea(String codLinea) {
		this.codLinea = codLinea;
	}
	public String getLineaNombre() {
		return lineaNombre;
	}
	public void setLineaNombre(String lineaNombre) {
		this.lineaNombre = lineaNombre;
	}
	public String getMaquina() {
		return maquina;
	}
	public void setMaquina(String maquina) {
		this.maquina = maquina;
	}
	public double getCapacidadNominal() {
		return capacidadNominal;
	}
	public void setCapacidadNominal(double capacidadNominal) {
		this.capacidadNominal = capacidadNominal;
	}

    
    
    
}
